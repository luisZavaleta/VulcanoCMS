	def createPortafolioItem(args){
		def parent = new Div(webpageId: args["webpageId"], selector:args["parentSelector"], clase:args["clase"], index: args["index"]).save(failOnError:true)

		new Img(webpageId: args["webpageId"], parentId:parent.id, selector: args["imageSelector"], src: args["imgSrc"], relativePath: true).save(failOnError:true)
		new H5(webpageId: args["webpageId"], parentId:parent.id, selector: args["titleSelector"], html: args["title"]).save(failOnError:true)
		new P(webpageId: args["webpageId"], parentId:parent.id, selector: args["textSelector"], html: args["text"]).save(failOnError:true)
	}
